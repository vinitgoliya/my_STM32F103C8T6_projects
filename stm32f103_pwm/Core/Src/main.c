void Generate_Sine_Table(void)
{
    for(int i = 0; i < SINE_SAMPLES; i++)
    {
        float angle = (2.0f * 3.14159265f * i) / SINE_SAMPLES;

        float sineValue = (sinf(angle) + 1.0f) / 2.0f;

        sineTable[i] = (uint16_t)(sineValue * PWM_PERIOD);
    }
}
